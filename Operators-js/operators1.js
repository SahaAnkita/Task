/* 1. 
Take two numbers and perform all the arithmetic operations over them.*/

// addition 

let number1 = 5;
let number2 = 2;
let add = number1 + number2;

console.log(add);

// subtraction

let number3 = 10;
let number4 = 5;
let subtraction = number3 - number4;

console.log(subtraction);

// multiplication

let number5 = 10;
let number6 = 5;
let multiplication = number5 * number6;

console.log(multiplication);

// division

let number7 = 10;
let number8 = 5;
let division = number7 / number8;

console.log(division);

// modulus / remainder

let number9 = 14;
let number10 = 3;
let modulus = number9 % number10;

console.log(modulus);

// exponentiation

let number11 = 3;
let number12 = 2;
let exponentiation = number11 ** number12;

console.log(exponentiation);

// increment 
// preincrement

let number13 = 3;
let preincrement = ++number13;

console.log(preincrement);

// postincrement

let number14 = 2;
let postincrement = number13++;

console.log(postincrement);

// decrement 
// predecrement

let number15 = 3;
let predecrement = --number15;

console.log(predecrement);

// postdecrement

let number16 = 2;
let postdecrement = number16--;

console.log(postdecrement);